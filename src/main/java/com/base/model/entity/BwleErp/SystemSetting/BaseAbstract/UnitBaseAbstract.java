package com.base.model.entity.BwleErp.SystemSetting.BaseAbstract;

public abstract class UnitBaseAbstract {
	private String unit_name;
	private String unit_enname;
	private String unit_ensname;
	private String unit_rate;
	private Integer unit_valid;
	public String getUnit_name() {
		return unit_name;
	}
	public void setUnit_name(String unit_name) {
		this.unit_name = unit_name;
	}
	public String getUnit_enname() {
		return unit_enname;
	}
	public void setUnit_enname(String unit_enname) {
		this.unit_enname = unit_enname;
	}
	public String getUnit_ensname() {
		return unit_ensname;
	}
	public void setUnit_ensname(String unit_ensname) {
		this.unit_ensname = unit_ensname;
	}
	public String getUnit_rate() {
		return unit_rate;
	}
	public void setUnit_rate(String unit_rate) {
		this.unit_rate = unit_rate;
	}
	public Integer getUnit_valid() {
		return unit_valid;
	}
	public void setUnit_valid(Integer unit_valid) {
		this.unit_valid = unit_valid;
	}
	
}
