package com.base.model.entity.BwleErp.SystemSetting.BaseAbstract;

public abstract class DepotBaseAbstract {
	private Integer depot_father_id;
	private String depot_name;
	private String depot_address;
	private String depot_mobile;
	private String depot_contact;
	private String depot_explain;
	private String depot_code;
	private String depot_region;
	public Integer getDepot_father_id() {
		return depot_father_id;
	}
	public void setDepot_father_id(Integer depot_father_id) {
		this.depot_father_id = depot_father_id;
	}
	public String getDepot_name() {
		return depot_name;
	}
	public void setDepot_name(String depot_name) {
		this.depot_name = depot_name;
	}
	public String getDepot_address() {
		return depot_address;
	}
	public void setDepot_address(String depot_address) {
		this.depot_address = depot_address;
	}
	public String getDepot_mobile() {
		return depot_mobile;
	}
	public void setDepot_mobile(String depot_mobile) {
		this.depot_mobile = depot_mobile;
	}
	public String getDepot_contact() {
		return depot_contact;
	}
	public void setDepot_contact(String depot_contact) {
		this.depot_contact = depot_contact;
	}
	public String getDepot_explain() {
		return depot_explain;
	}
	public void setDepot_explain(String depot_explain) {
		this.depot_explain = depot_explain;
	}
	public String getDepot_code() {
		return depot_code;
	}
	public void setDepot_code(String depot_code) {
		this.depot_code = depot_code;
	}
	public String getDepot_region() {
		return depot_region;
	}
	public void setDepot_region(String depot_region) {
		this.depot_region = depot_region;
	}
	
}
