package com.base.model.vo.BwleErp.Procurement;

public class SupplierLawsuit {
	private String plaintiff;
	private String defendant;
	private String amount;
	private String judgment_date;
	private String record_date;
	private String remark;
	public String getPlaintiff() {
		return plaintiff;
	}
	public void setPlaintiff(String plaintiff) {
		this.plaintiff = plaintiff;
	}
	public String getDefendant() {
		return defendant;
	}
	public void setDefendant(String defendant) {
		this.defendant = defendant;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getJudgment_date() {
		return judgment_date;
	}
	public void setJudgment_date(String judgment_date) {
		this.judgment_date = judgment_date;
	}
	public String getRecord_date() {
		return record_date;
	}
	public void setRecord_date(String record_date) {
		this.record_date = record_date;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
