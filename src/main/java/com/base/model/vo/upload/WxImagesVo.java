package com.base.model.vo.upload;

public class WxImagesVo {
	private String url;
	private String name;
	private String extname;
	private String file_use_type;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getExtname() {
		return extname;
	}
	public void setExtname(String extname) {
		this.extname = extname;
	}
	public String getFile_use_type() {
		return file_use_type;
	}
	public void setFile_use_type(String file_use_type) {
		this.file_use_type = file_use_type;
	}
}
