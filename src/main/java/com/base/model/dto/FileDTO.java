package com.base.model.dto;

public class FileDTO {
	protected String file_name;
	protected String file_type;
	protected String file_url;
	protected String file_use_type;
	protected Integer file_size;
	protected String file_extend;
	protected String file_datetime;

	public String getFile_name() {
		return file_name;
	}

	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}

	public String getFile_type() {
		return file_type;
	}

	public void setFile_type(String file_type) {
		this.file_type = file_type;
	}

	public String getFile_url() {
		return file_url;
	}

	public void setFile_url(String file_url) {
		this.file_url = file_url;
	}

	public String getFile_use_type() {
		return file_use_type;
	}

	public void setFile_use_type(String file_use_type) {
		this.file_use_type = file_use_type;
	}

	public Integer getFile_size() {
		return file_size;
	}

	public void setFile_size(Integer file_size) {
		this.file_size = file_size;
	}

	public String getFile_extend() {
		return file_extend;
	}

	public void setFile_extend(String file_extend) {
		this.file_extend = file_extend;
	}

	public String getFile_datetime() {
		return file_datetime;
	}

	public void setFile_datetime(String file_datetime) {
		this.file_datetime = file_datetime;
	}
	
	
}
